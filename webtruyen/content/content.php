        <p>Đại lục Thương Vũ , biên giới phía Tây, thành Chiêu An.

Ở trước cổng Tư gia, một thiếu nữ đang bạo dạn giằng co cùng Ngũ trưởng lão của Tư gia.

Lúc này, sắc mặt của hai người đều cực kỳ khó coi, ánh mắt nhìn chằm chằm của song phương đều âm trầm giống như có đại thù sống chết gì đó.

Cách đó không xa, một vài kẻ lắm mồm nhìn xa xa, lén lút chỉ trỏ, âm thầm bội phục cô thiếu nữ kia, thế nhưng dám can đảm gây ầm ỹ với Tư gia. Ai mà không biết Tư gia này là một trong ba Đại thế gia tu tiên của thành Chiêu An, trong tộc có Kim Đan lão tổ trấn thủ. Những tu sĩ bình thường gặp phải Tư gia đều sẽ lễ ngộ vài phần, tuyệt đối không dám chống đối Tư gia.

Lại nhìn cô gái kia, bộ dáng xinh đẹp rực rỡ, tuy rằng biết người tu tiên có linh khí dưỡng thân thể, được trời cao ưu ái; bộ dạng, khí chất đều là cao cấp, nhưng dung nhan thiếu nữ này phải diễm lệ hơn vài phần so với những nữ tu sĩ tầm thường, khí chất cũng hết sức cao quý, nhìn xa xa, tiên tư phiêu dật, đúng là khó được. Lại nhìn tu vi, càng làm người chấn động, nhìn linh cốt bất quá chỉ 17 tuổi, thế nhưng đã là luyện khí kỳ tầng mười. Nếu không ngoài ý muốn, phỏng chừng trước hai mươi tuổi, nàng ta tuyệt đối có thể thành công đến Trúc Cơ, trở thành một trong những Trúc Cơ tu sĩ trẻ tuổi của Tây Cảnh này.

Tại nơi Tây Cảnh với linh khí mỏng manh này, tu sĩ Trúc Cơ mà hai mươi tuổi tuyệt đối có thể được xưng là thế hệ kinh tài tuyệt diễm rồi.

Rất nhanh, lại có người nhận ra cô thiếu nữ này chính là đệ tử của Nguyệt gia, cũng chính là ba Đại thế gia tu tiên còn lại của Thành Chiêu An, tên là Nguyệt Thiên Dạ.

Nói đến thì cô Nguyệt Thiên Dạ này cũng là truyền kỳ. Nghe nói trước năm 16 tuổi, nàng ta chỉ là kẻ ngũ linh căn hỗn tạp, dù tu luyện đến 16 tuổi nhưng vẫn chỉ là luyện khí kỳ tầng hai - tu vi mạt đẳng. Cái linh căn này nếu không có công pháp cao cấp cùng với rất nhiều linh đan thượng phẩm, dù có cố gắng tu luyện đi nữa thì cũng chỉ có thể dừng lại ở Kim Đan kỳ, hơn nữa tiến độ vô cùng chậm chạp.

Ai biết vào thời điểm khi Nguyệt Thiên Dạ 16 tuổi, đang lúc tu luyện thì nàng ta bị xá khí (khí bị rẽ sang hướng khác), thiếu chút nữa về chầu trời, bất quá may mắn, cuối cùng chỉ là một trận sợ bóng sợ gió. Sau đó không lâu, Nguyệt Thiên Dạ dường như đột nhiên chiếm được cái cơ duyên trời cho gì đó, tu vi giống như được đập tiên đan vậy, tăng lên vùng vụt. Chỉ trong một năm, giúp cho nàng ta kiên cường tu luyện đến luyện khí kỳ tầng mười, trở thành thiên tài không thể bỏ qua.

Bất quá, tuy rằng Nguyệt Thiên Dạ có vẻ là nhân vật thiên tài, nhưng ở thành Chiêu An, ai mà không biết Thiên tài chân chính phải nói tới là Song Tử của Tư gia. Đây mới thực sự là nhân vật thiên tài. 15 tuổi đã là tu sĩ Trúc Cơ, cho dù là ở đại lục Trung Ương cũng ít gặp, kể cả ở trong những đại tông môn kia, cũng có thể gọi là con cưng của trời.

Thế mà ở Tư gia lại xuất hiện hai thiên tài đồng lứa. Một người tên Tư Hàn, là biến dị trời sinh - Băng linh căn, 15 tuổi đã là Trúc Cơ; năm nay hai mươi tuổi, đã là tu sĩ Trúc Cơ trung kỳ. Một người khác tên Tư Lăng, là đơn linh căn hệ hoả, cũng 15 tuổi đạt Trúc Cơ; năm nay vừa tròn 18 tuổi, là tu sĩ Trúc Cơ sơ kỳ đỉnh cao.

Nhưng mà, làm người ta đáng tiếc chính là; vào tháng trước, khi Tiểu Thánh Cảnh của Tây Cảnh mở ra, Tư Lăng dẫn dắt đệ tử của gia tộc, cùng với các đệ tử khác của Tam Đại thế gia Thành Chiêu Dương An, cùng nhau tiến vào Tiểu Thánh Cảnh lịch lãm. Theo người ta kể thì, ở Tiểu Thánh Cảnh gặp phải một con yêu thú cấp 6, vì cứu một tên đệ tử của Nguyệt gia, hắn bị trọng thương, tu vi cũng hạ xuống tới Trúc Cơ sơ kỳ.

Cái này còn chưa tính, chuyện xui còn ở phía sau kia.

Mà ngay mới vừa rồi, Nguyệt Thiên Dạ đến Tư gia cầu kiến Tư Lăng, ai ngờ lại gây xung đột với thị vệ giữ cửa của Tư gia. Nàng ta muốn xông vào Tư gia, lại không khéo đi đánh nhau với Ngũ trưởng lão của Tư gia - Tư Danh - vừa về đến nhà.

Tư Danh này năm nay đã 150 tuổi, là tu sĩ Trúc Cơ hậu kỳ, nào phải là người mà một kẻ hậu bối luyện khí kỳ tầng mười có thể khiêu khích chứ? Chỉ là Nguyệt Thiên Dạ cũng ngông nghênh trời sinh, tuy rằng tu vi thấp, nhưng lại không sợ tu sĩ Trúc Cơ, khí thế bức người, không chịu lui về sau, tuyên bố muốn xông vào Tư gia. Một tên tiểu bối cũng dám làm ầm ĩ với hắn, Tư Danh tự nhiên không khách khí, vốn là muốn trực tiếp chém giết -- tại thế giới Tu Tiên, tu sĩ cấp thấp không biết tự lượng sức, chọc phải tu sĩ cấp cao, bị giết cũng không ai đồng tình -- nhưng ngay thời điểm khi hắn xuất thủ, Tư Lăng không biết làm thế nào lại xuất hiện, gắng gượng thay Nguyệt Thiên Dạ nhận sát chiêu của hắn.

Tư Danh là tu sĩ Trúc Cơ hậu kỳ thành danh đã lâu, có được loại Khí bổn mạng của mình, coi như chỉ dùng tám phần linh lực để đánh ra Khí Ngũ tuyệt, cũng đâu phải kẻ đang bị trọng thương như Tư Lăng có thể thừa nhận chứ?

Sự tình phát sinh quá đột ngột, không ai có thể dự liệu rằng Tư Lăng - thiên tài của Tư gia - lại sẽ vì cứu một thiếu nữ không liên quan mà liều mạng đến có khả năng bị phế toàn bộ linh căn. Kết cục có thể tưởng tượng này khiến bao nhiêu người than thở không thôi. Mắt thấy Tư Lăng bị choáng ngay tại chỗ, mọi người ở chỗ này đều có thể cảm thấy rõ khí tức của Tư Lăng đang yếu dần đi.

“Ông -- ông lại làm hắn bị thương như thế!!” Đôi mắt đẹp của Nguyệt Thiên Dạ chớp chớp phẫn nộ, oán độc nhìn chằm chằm Tư Danh.

Lúc này Tư Danh cũng đại loạn trong lòng, vội vàng chỉ huy người Tư gia chuyển Tư Lăng đã hôn mê về Tư gia. Lúc này hắn cũng đầy một bụng hỏa đối với Nguyệt Thiên Dạ, thậm chí sát ý càng lớn. Sự tình này nói tới nói lui, vẫn là do Nguyệt Thiên Dạ không biết tự lượng sức xông vào Tư gia mới biến thành tình cảnh như vậy.

Tư Danh lo lắng thương thế của Tư Lăng, đâu còn bình tĩnh quan tâm nàng ta, lập tức phất tay áo một cái, quát một tiếng: “Cút!”

Sóng âm kéo dài, giống như có hình có dạng, khiến cho toàn thân Nguyệt Thiên Dạ bị đánh bay ra ngoài.

Nguyệt Thiên Dạ quỳ rạp trên mặt đất, bị uy áp[1] của tu sĩ Trúc Cơ dè nén khiến cho không cách nào nhổm dậy, trong lòng tràn đầy khuất nhục xấu hổ. Càng làm cho nàng khó chịu là, Tư Lăng đã vì nàng mà nhiều lần bị thương nặng. Nam nhân tình sâu nghĩa nặng như thế, sao không khiến lòng nàng rung động, cảm động triền miên được chứ? Cả hai đời cộng lại, đây là nam tử duy nhất nguyện ý vì nàng mà hi sinh tính mạng .

[1]Uy áp: Áp lực của uy phong.

Nhưng mà...

Tư Lăng, vì sao chàng lại ngốc như vậy?

Không ai quan tâm tới thiếu nữ bị ngã xuống đất này, dù sao trong mắt người bình thường, Nguyệt Thiên Dạ thật là kẻ không biết tự lượng sức. Rõ ràng là tu sĩ cấp thấp, lại là kẻ không tự biết thân, dám sinh sự ở cửa lớn Tư gia còn chưa tính, lại dám khiêu khích Ngũ trưởng lão Tư gia. Nếu không phải đúng lúc đó thiên tài Tư gia xảy ra chuyện, người Tư gia đã sớm nhân cơ hội chém chết nàng ta ngay trước cửa để cảnh cáo rồi. Tin chắc người ở Nguyệt gia cũng hiểu được nguyên do, sẽ không vì tên đệ tử không lượng sức mà chống lại Tư gia.

Tư gia náo động cả lên, cả tộc trưởng cũng hoảng sợ mà xuất quan thăm hỏi. Đợi khi biết được Tư Lăng gặp chuyện không may, tộc trưởng Tư gia cắn hàm răng ngà đến phát ra tiếng két két.
